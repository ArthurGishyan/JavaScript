//math round Returns x rounded to its nearest integer
let a = 6.3
console.log(Math.round(a)); //6

//math Returns x rounded up to its nearest integer
console.log(Math.ceil(a)); //7

//math floor Returns x rounded down to its nearest integer
console.log(Math.floor(a)); //6

//math trunk returns main int part

console.log(Math.trunc(a)); //6

//math sign return 1 if the variable is positive, returns -1 if variable is negative and returns 0 if values is 0
console.log(Math.sign(a));

